//! Math.floor()

console.log(Math.floor(24.8)); // 24
console.log("*************");

//! Math.round()

console.log(Math.round(12.6)); // 13
console.log("*************");

//! Math.trunc()

console.log(Math.trunc(12.6)); // 12
console.log("**************");

//! Math.ceil()

console.log(Math.ceil(12.3)); // 13
console.log("***************");

//! Math.pow()

console.log(Math.pow(3,4));  // 81
console.log("***********");


//! Math.max() 
console.log(Math.max(1,3,34,67,89,9,0,6,56,4,5)); // 89
console.log("*************");

//! Math.min()
console.log(Math.min(1,3,34,67,89,9,0,6,56,4,5)); // 0
console.log("*************");

//! Math.sqrt()

console.log(Math.sqrt(4)); // 2
console.log("*************");

//! Math.abs()
// abs = absolute
console.log(Math.abs(-8)); // 8
console.log("*************");

//! Math.random()

console.log(Math.random());
console.log("*************");

//! four digit random number

let otp = Math.floor(Math.random()*10000);
